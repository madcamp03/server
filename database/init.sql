-- players_record_pitcher 테이블 생성
CREATE TABLE IF NOT EXISTS players_record_pitcher (
    record_id INTEGER PRIMARY KEY,
    player_id INTEGER,
    player_name VARCHAR(255),
    team_id INTEGER,
    team VARCHAR(255),
    season INTEGER,
    era DOUBLE, -- 평균자책점
    game_count INTEGER, -- 경기 수
    win INTEGER, -- 승수
    lose INTEGER, -- 패수
    sv INTEGER, -- 세이브 수
    hld INTEGER, -- 홀드 수
    ip VARCHAR(255), -- 이닝 수
    hr INTEGER, -- 피홈런 수
    bb INTEGER, -- 볼넷 수
    hbp INTEGER, -- 사구 수
    so INTEGER, -- 삼진 수
    run INTEGER, -- 실점
    er INTEGER, -- 자책점
    whip DOUBLE, -- 이닝당 출루 허용률
    wpct DOUBLE, -- 승률
    hit INTEGER, -- 피안타 수
    FOREIGN KEY (player_id) REFERENCES players_info_pitcher(player_id)
);

-- players_record_hitter 테이블 생성

CREATE TABLE IF NOT EXISTS players_record_hitter (
    record_id INTEGER PRIMARY KEY,
    player_id INTEGER,
    player_name VARCHAR(255),
    team_id INTEGER,
    team VARCHAR(255),
    season INTEGER,
    game_count INTEGER,
    bat_avg DECIMAL(5, 3), -- 타율
    slg DECIMAL(5, 3), -- 출루율
    obp DECIMAL(5, 3), -- 장타율
    ops DECIMAL(5, 3), -- ops
    pa INTEGER, -- 타석
    ab INTEGER, -- 타수
    run INTEGER, -- 득점
    hit INTEGER, -- 안타
    hit_base2 INTEGER, -- 2루타
    hit_base3 INTEGER, -- 3루타
    homerun INTEGER, -- 홈런
    rbi INTEGER, -- 타점
    sac INTEGER, -- 희생번트
    sf INTEGER, -- 희생플라이
    bb INTEGER, -- 볼넷
    so INTEGER, -- 삼진
    risp DECIMAL(5, 3), -- 득점권 타율
    sb INTEGER, -- 도루 성공
    sb_percent DOUBLE, -- 도루성공률
    FOREIGN KEY (player_id) REFERENCES players_info_hitter(player_id)
);


